string const oems_m::write_touchstone_m(
	"function write_touchstone( type, freq, data, filename, ref, comment )\n"
	"% write_touchstone( type, freq, data, filename [,ref [,comment]] ) - write touchstone-file\n"
	"% for ADS\n"
	"% type      'z' for Z-Parameter touchstone-file\n"
	"%           'y' for Y-Parameter touchstone-file\n"
	"%           's' for S-Parameter touchstone-file\n"
	"% freq      vector of frequencies (in Hz)\n"
	"% data      (numports x numports x numfreq) matrix\n"
	"% filename  filename of the file to create\n"
	"% ref       (optional) reference impedance\n"
	"% comment   (optional) written in the header\n"
	"%\n"
	"% (C) Sebastian Held <sebastian.held@gmx.de>\n"
	"% Version 21. Feb 2006\n"
	"\n"
	"if lower(type) == 'z'\n"
	"    par_type = 'Z';\n"
	"    if nargin < 5; ref = 1; end\n"
	"elseif lower(type) == 'y'\n"
	"    par_type = 'Y';\n"
	"    if nargin < 5; ref = 1; end\n"
	"elseif lower(type) == 's'\n"
	"    par_type = 'S';\n"
	"    if nargin < 5; ref = 50; end\n"
	"else\n"
	"    error( 'only z-, y- and s-parameters supported by now' );\n"
	"end\n"
	"\n"
	"if nargin < 6\n"
	"    comment = '';\n"
	"end\n"
	"\n"
	"num_rows = size( data, 1 );\n"
	"num_columns = size( data, 2 );\n"
	"if num_rows ~= num_columns\n"
	"    error( 'number of rows and columns do not agree' );\n"
	"end\n"
	"\n"
	"[fid, message] = fopen( filename, 'wt' );\n"
	"if fid == -1\n"
	"    error( message );\n"
	"end\n"
	"\n"
	"fprintf( fid, '%s\\n', ['#   Hz   ' par_type '  RI   R   ' num2str(ref)] );\n"
	"fprintf( fid, '%s\\n', ['! ' comment] );\n"
	"\n"
	"if size( data, 1 ) == 1 || size( data, 1 ) >= 3\n"
	"    write_data_1(data, fid, freq);\n"
	"elseif size( data, 1 ) == 2\n"
	"    write_data_2(data, fid, freq);\n"
	"else\n"
	"    error( 'FIXME - unhandled dimension' );\n"
	"end\n"
	"\n"
	"fclose( fid );\n"
	"return;\n"
	"end %write_touchstone()\n"
	"\n"
	"function write_data_1(data, fid, freq)\n"
	"num_rows = size( data, 1 );\n"
	"num_columns = size( data, 2 );\n"
	"% for more than 3-port devices\n"
	"for f=1:length(freq)\n"
	"    fprintf( fid, '%e ', freq(f) ); %Frequenz\n"
	"    for row = 1:num_rows\n"
	"        column = 1;\n"
	"        while column <= num_columns\n"
	"            for z=1:min( 4, num_columns-column+1 ); % nur maximal 4 Parameter in einer Zeile! (ADS-Begrenzung)\n"
	"                fprintf( fid, '% e % e   ', real( data(row,column,f) ), imag( data(row,column,f) ) );\n"
	"                column = column + 1;\n"
	"            end\n"
	"            fprintf( fid, '\\n             ' ); % Zeilenende\n"
	"        end\n"
	"    end\n"
	"    fprintf( fid, '\\n' );\n"
	"end\n"
	"end %write_data_1()\n"
	"\n"
	"function write_data_2(data, fid, freq)\n"
	"% for 2-port devices\n"
	"% ADS uses different format (what a mess)\n"
	"for f=1:length(freq)\n"
	"    fprintf( fid, '%e   % e % e   % e % e   % e % e   % e % e\\n', freq(f), ...\n"
	"        real(data(1,1,f)), imag(data(1,1,f)), real(data(2,1,f)), imag(data(2,1,f)), ...\n"
	"        real(data(1,2,f)), imag(data(1,2,f)), real(data(2,2,f)), imag(data(2,2,f)) ); %Frequenz\n"
	"end\n"
	"end %write_data_2()\n");

